    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed

       DefaultTableModel model = (DefaultTableModel) tbData.getModel();

    // Membuat array objek baru berisi data yang akan ditambahkan
        Object[] rowData = {tfNama.getText(), tfAlamat.getText(), tfTelepon.getText(), tfRelasi.getText()};
    // Menambahkan data ke model tabel
        model.addRow(rowData);
    // Membersihkan input field setelah menambahkan data
        clearFields();
    // Menyimpan perubahan ke dalam file
        saveDataToFile();
    }//GEN-LAST:event_btnTambahActionPerformed

    private void loadDataFromFile() {
        try (BufferedReader br = new BufferedReader(new FileReader(FILE_PATH))) {
            String line;
            DefaultTableModel model = (DefaultTableModel) tbData.getModel();
            model.setRowCount(0);
    // Mengosongkan tabel sebelum memuat data baru

            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");
                model.addRow(data);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }  
    }
        
    private void saveDataToFile() {
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(FILE_PATH))) {
            for (int row = 0; row < tbData.getRowCount(); row++) {
                for (int col = 0; col < tbData.getColumnCount(); col++) {
                    bw.write(tbData.getValueAt(row, col).toString());
                    if (col < tbData.getColumnCount() - 1) {
                        bw.write(",");
                    }
                }
                bw.newLine();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
