    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (selectedRow >= 0) {
            // Edit data pada tabel
            tbData.setValueAt(tfNama.getText(), selectedRow, 0);
            tbData.setValueAt(tfAlamat.getText(), selectedRow, 1);
            tbData.setValueAt(tfTelepon.getText(), selectedRow, 2);
            tbData.setValueAt(tfRelasi.getText(), selectedRow, 3);

            // Menampilkan pesan validasi "Data berhasil disimpan"
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // Menyimpan perubahan ke dalam file
            saveDataToFile();
            clearFields();
            selectedRow = -1;
        } else {
            // Menampilkan pesan jika tidak ada baris yang dipilih
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diedit", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int selectedRow = tbData.getSelectedRow();
        if (selectedRow >= 0) {
            // Hapus data dari tabel
            ((javax.swing.table.DefaultTableModel) tbData.getModel()).removeRow(selectedRow);

            // Simpan perubahan ke dalam file
            saveDataToFile();
            clearFields();
            selectedRow = -1;

            // Menampilkan pesan validasi "Data berhasil dihapus"
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Menampilkan pesan jika tidak ada baris yang dipilih
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

